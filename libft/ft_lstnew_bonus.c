#include "libft.h"

t_list	*ft_lstnew(void *content)
{
	t_list	*dst;

	dst = (t_list *)malloc(sizeof(t_list));
	if (!dst)
		return (0);
	dst->content = content;
	dst->next = NULL;
	return (dst);
}
